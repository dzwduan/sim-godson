//------------------- ram_ff ---------------------------------------------------------
//ram constructed by flip-flops, for control components like queues. 
//paramter wr_port: number of write ports
//paramter rd_port: number of read ports
//paramter width: the data width of ram
//paramter depth: the data depth of ram

void power_ram_ff(struct power_stat_ram * return_power,int gated_clock,int Wport,int Rport,int width,int depth)
{
  int drive = X4;
  int decode_index = (depth<= 4)?  0 : (depth<=  8)?  1 :
                     (depth<=16)?  2 : (depth<= 32)?  3 :
                     (depth<=64)?  4 : (depth<=128)?  5 : 6; 
  int Wport_index = (Wport > 10)? 10 : Wport;  
  int Rport_index = (Rport > 10)? 10 : Rport;
  int depth_index = (depth<=  2)?   0 : (depth<=   4)?   1 :
                    (depth<=  8)?   2 : (depth<=  12)?   3 :
                    (depth<= 16)?   4 : (depth<=  24)?   5 :  
                    (depth<= 32)?   6 : (depth<=  48)?   7 :
                    (depth<= 64)?   8 : (depth<=  96)?   9 : 
                    (depth<=128)?  10 : (depth<= 256)?  11 : 
                    (depth<=512)?  12 : (depth<=1024)?  13 : 14;

  return_power->inst = Wport* power_table_decode[decode_index].inst
                      +width* power_table_ram_write[Wport_index][depth_index].inst  
                      +width* depth* power_table_reg_scan_en[drive].inst
                      +Rport* power_table_decode[decode_index].inst
                      +width * power_table_ram_read[Rport_index][depth_index].inst;

  return_power->reg  = width* depth;
                      
  return_power->area = Wport* power_table_decode[decode_index].area
                      +width* power_table_ram_write[Wport_index][depth_index].area  
                      +width* depth* power_table_reg_scan_en[drive].area
                      +Rport* power_table_decode[decode_index].area
                      +width * power_table_ram_read[Rport_index][depth_index].area;

  return_power->leakage = Wport* power_table_decode[decode_index].leakage
                         +width* power_table_ram_write[Wport_index][depth_index].leakage 
                         +width* depth* power_table_reg_scan_en[drive].leakage
                         +Rport* power_table_decode[decode_index].leakage
                         +width * power_table_ram_read[Rport_index][depth_index].leakage;

  return_power->dyn_write = power_table_decode[decode_index].dynamic
                           +width* power_table_ram_write[Wport_index][depth_index].dynamic  
                           +width* power_table_reg_scan_en[drive].dynamic
                           +width* power_table_ram_read[Rport_index][depth_index].dyn_data;
  
  return_power->dyn_read = power_table_decode[decode_index].dynamic
                          +width* power_table_ram_read[Rport_index][depth_index].dyn_sel;

  if (gated_clock)
    return_power->clock = width* power_table_reg_scan_en[drive].clock;
  else                       
    return_power->clock = width* depth * power_table_reg_scan_en[drive].clock;
}

